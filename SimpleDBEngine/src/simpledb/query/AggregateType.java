package simpledb.query;

/**
 * The enum Aggregate type.
 */
public enum AggregateType {
    /**
     * Max aggregate type.
     */
    MAX,
    /**
     * Min aggregate type.
     */
    MIN,
    /**
     * Sum aggregate type.
     */
    SUM,
    /**
     * Count aggregate type.
     */
    COUNT,
    /**
     * Avg aggregate type.
     */
    AVG
}